#include <unistd.h>

void	put_str(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		write(1, &str[i], 1);
		i++;
	}
}

int	main(int argc, char *argv[])
{
	int	i;

	i = argc - 1;
	if (argc >= 1)
	{
		while (i > 0)
		{
			put_str(argv[i]);
			write(1, "\n", 1);
			i--;
		}
	}
	return (0);
}
